# True, False 극혐
print(True)
print(False)

# 대소비교(부등호)
print(3>1)

# 값 비교(숫자, 문자열)
print(1 == 1)
print('str' != 'str')

# 객체 비교
print(1 == 1.0) # 값 비교
print(1 is 1.0) # 객체 비교
print(1 is not 1.0) # 객체 비교

# 값 비교에는 is 를 쓰면 안 됨

# boolean 이 아닌 타입의 bool
print(bool(1))  # 값이 있다라고 생각 -> True
print(bool(0))  # 값이 없다라고 생각 -> False
print(bool('False'))    # 값이 있다라고 생각 -> True

